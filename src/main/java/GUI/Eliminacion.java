    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        String estado = txESTADO.getText();
        
        if (!estado.equals("0")) {
            JOptionPane.showMessageDialog(this, "Error: El estado debe ser '0'.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String sql = "UPDATE arbolesbin SET DATO=?, ESTADO=? WHERE ID=?";
        PreparedStatement psActualizar = con.prepareStatement(sql);
        psActualizar.setString(1, txDATO.getText());
        psActualizar.setString(2, estado);
        psActualizar.setString(3, txID.getText());
        psActualizar.executeUpdate();
        con.close();
        System.out.println("Actualizaci√≥n exitosa");
    } catch (SQLException ex) {
        System.err.println("Error al actualizar: " + ex.getMessage());
    }
    dispose();
    Arbolesbinarios vArbolesBin = new Arbolesbinarios();
    }//GEN-LAST:event_jButton1ActionPerformed

